                name = "Прыжки со скакалкой",
                description = "До начала выполнения упражнения нужно закинуть скакалку за спину , слегка согуть руки в локтях и отвести кисти на 15-20 сантиметров от бедер. Находясь в данном положение начинаем вращать скакалку. Описывайте небольшие круги руками: в движении участвуют как кисти, так и руки. Скакалка должна всего лишь слегка касаться пола, чтобы ритм не замедлялся. Соблюдайте отрыв в пару сантиметров от змели чтобы не мешать вращению скакалки."
