            )
        )
        initList.add(
            Exercise(
                name = "Подъем на носки стоя",
                description = "Нужно встать на твердую ровную поверхность. Ноги должны быть на ширине или чуть уже плеч. Для удержания равновесия желательно удерживаться руками за любой удобный предмет. Желательно нужно совсем не много согнуть колени , для избежения получения травм.  В исходном положении нужно напрячь мышцы голени. Затем нужно начать поднимать пятки, но не быстро. На то, чтобы достигнуть верхней точки, должно уйти 1-2 секунды. На подъеме следует совсем ненадолго остановиться. После чего возращаемся в исходное положение."
