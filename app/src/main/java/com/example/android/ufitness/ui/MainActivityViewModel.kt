            )
        )
        initList.add(
            Exercise(
                name = "Отжимания",
                description = "Лечь на горизонтальную ровную поверхность. Руки при этом должны быть на ширине плеч, а стопы — таза. Нужно следить за тем, чтобы пальцы ног упирались в пол, а ладони были направлены вверх.Руки на вдохе сгибаются в локтевых суставах, корпус опускается вниз таким образом, чтобы он образовывал ровную и прямую линию, которая должна быть равномерной и не ломаться. Возврат в исходную позицию выполняется на выдохе."
